
console.log(Math.abs(-5.5));  // Kết quả: 5.5 (loại bỏ dấu âm)
console.log(Math.abs(10));  // Kết quả: 10 (số dương giữ nguyên)

console.log(Math.ceil(4.4)); // Kết quả: 5 (làm tròn lên số nguyên gần nhất)
console.log(Math.ceil(5.8)); // Kết quả: 6 (làm tròn lên số nguyên gần nhất)

console.log(Math.floor(5.7)); // Kết quả: 5 (làm tròn xuống số nguyên gần nhất)
console.log(Math.floor(6.1)); // Kết quả: 6 (làm tròn xuống số nguyên gần nhất)

console.log(Math.round(10.1)); // Kết quả: 10 (gần 10 hơn nên làm tròn xuống)
console.log(Math.round(20.6)); // Kết quả: 21 (gần 21 hơn nên làm tròn lên)